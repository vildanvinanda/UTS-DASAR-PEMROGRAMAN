#include <iostream>
using namespace std;

int main ()
{
    int k;
    float l;

    k = 100;
    l = 10.5;

    cout << "Nilai k Awal   : " << k << endl;
    cout << "Nilai --k      : " << --k << endl;
    cout << "Nilai k Akhir  : " << k << endl << endl;

    cout << "Nilai l Awal   : " << l << endl;
    cout << "Nilai l--      : " << l-- << endl;
    cout << "Nilai 1 Akhir  : " << l << endl << endl;

    return 0;

}
